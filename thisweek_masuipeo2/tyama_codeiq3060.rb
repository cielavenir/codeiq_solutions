#!/usr/bin/env ruby
$memo={}
def dfs(n,prev)
	return 1 if n==0
	$memo[[n,prev]]||=(1..n).reduce(0){|s,i|
		s+(i==prev ? 0 : dfs(n-i,i))
	}
end
p dfs(gets.to_i,-1)

__END__
Conway Sequenceを2桁ずつに区切る時、回文数であるならば、左側を決めれば右側は一意に求まる。
そして、左側の数字の和は元の数の桁数に等しい。
したがって、問題の答えは、入力値を整数の和(ただし隣り合う要素は異なる)に分割する方法の数に等しい。
なお、この方法で左側を決める場合、同じ数字が(n進法表記の時)n個以上連続する場合については繰り上がりにより考慮することができないため、n>=10の入力に対しても答えを出力することは一応できるが、10進法では正しくないことに注意を要する。
