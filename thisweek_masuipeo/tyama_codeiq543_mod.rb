#!/usr/bin/ruby
#coding:utf-8
M=[0,31,29,31,30,31,30,31,31,30,31,30,31]
N=20000000.to_s(2).size # 25
a=[]
(1<<(N+1)/2).times{|i|
	s='%0*b'%[(N+1)/2,i]
	s=s+s[0,N/2].reverse
	n=s.to_i(2)
	y=n/10000;m=n/100%100;d=n%100
	a<<n if 1964<=y&&y<=2020 && 0<m&&m<=12 && 0<d&&d<=M[m]
}
puts a*',' # 19660713,19660905,19770217,19950617,20020505,20130201