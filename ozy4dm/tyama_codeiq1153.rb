#!/usr/bin/ruby
N=gets.to_i
a=Enumerator.new{|y|
	a=b=1
	y<<a
	loop{
		y<<b
		a,b=b,a+b
	}
}.take_while{|e|e<=N}
puts "#{a.size-2} #{a[-1]} #{a[-2]}"
__END__
